<h1>Sobre Nosotros</h1>

<p>
La idea de crear una nueva institución para que a través de ella se proyectara un nuevo mercado de flores y plantas comenzó a gestarse en junio de 1998. Esta iniciativa tuvo una inmediata y masiva adhesión. Fue así que en apenas cinco meses, el 2 de diciembre de ese mismo año, se inauguraron las primeras instalaciones de la entonces flamante cooperativa Mercoflor. Ese día comenzó a funcionar el nuevo mercado. 
Su primer presidente fue el ingeniero José Luis Leiva.

Hoy Mercoflor se sitúa como uno de los mercados mayoristas más importantes del país.

Pero Mercoflor continúa creciendo y, en la actualidad, se están diagramando varias obras para la ampliación del sector de comercialización de plantas ornamentales, en el cual se espera contar con nuevos socios viveristas.
</p>
